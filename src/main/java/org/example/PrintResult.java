package org.example;

public class PrintResult {
    public static void printResult(int iterativeResult, int recursiveResult, int dynamicResult) {
        System.out.println("Iterative Fibonacci result: " + iterativeResult);
        System.out.println("Recursive Fibonacci result: " + recursiveResult);
        System.out.println("Dynamic Programming Fibonacci result: " + dynamicResult);
    }
}
